const { diamond } = require("./diamond.js");
describe("diamond", () => {
  //empty Case
  it("should return an empty string on ''", () => {
    expect(diamond("")).toEqual([]);
  });
  //A-Diamond
  it("should return A on 'A'", () => {
    expect(diamond("A")).toEqual(["A"]);
  });
  //B-Diamond
  it("should return the B-Diamond on 'B'", () => {
    expect(diamond("B")).toEqual(["-A-", "B-B", "-A-"]);
  });
  //C-Diamond
  it("should return the C-Diamond on 'C'", () => {
    expect(diamond("C")).toEqual(["--A--", "-B-B-", "C---C", "-B-B-", "--A--"]);
  });
  //D-Diamond
  it("should return the D-Diamond on 'D'", () => {
    expect(diamond("D")).toEqual([
      "---A---",
      "--B-B--",
      "-C---C-",
      "D-----D",
      "-C---C-",
      "--B-B--",
      "---A---",
    ]);
  });
  //E-Diamond
  it("should return the E-Diamond on 'E'", () => {
    expect(diamond("E")).toEqual([
      "----A----",
      "---B-B---",
      "--C---C--",
      "-D-----D-",
      "E-------E",
      "-D-----D-",
      "--C---C--",
      "---B-B---",
      "----A----",
    ]);
  });
  //F-Diamond
  it("should return the F-Diamond on 'F'", () => {
    expect(diamond("F")).toEqual([
      "-----A-----",
      "----B-B----",
      "---C---C---",
      "--D-----D--",
      "-E-------E-",
      "F---------F",
      "-E-------E-",
      "--D-----D--",
      "---C---C---",
      "----B-B----",
      "-----A-----",
    ]);
  });
  //Z-Diamond
  it("should return the Z-Diamond on 'Z'", () => {
    expect(diamond("Z")).toEqual([
      "-------------------------A-------------------------",
      "------------------------B-B------------------------",
      "-----------------------C---C-----------------------",
      "----------------------D-----D----------------------",
      "---------------------E-------E---------------------",
      "--------------------F---------F--------------------",
      "-------------------G-----------G-------------------",
      "------------------H-------------H------------------",
      "-----------------I---------------I-----------------",
      "----------------J-----------------J----------------",
      "---------------K-------------------K---------------",
      "--------------L---------------------L--------------",
      "-------------M-----------------------M-------------",
      "------------N-------------------------N------------",
      "-----------O---------------------------O-----------",
      "----------P-----------------------------P----------",
      "---------Q-------------------------------Q---------",
      "--------R---------------------------------R--------",
      "-------S-----------------------------------S-------",
      "------T-------------------------------------T------",
      "-----U---------------------------------------U-----",
      "----V-----------------------------------------V----",
      "---W-------------------------------------------W---",
      "--X---------------------------------------------X--",
      "-Y-----------------------------------------------Y-",
      "Z-------------------------------------------------Z",
      "-Y-----------------------------------------------Y-",
      "--X---------------------------------------------X--",
      "---W-------------------------------------------W---",
      "----V-----------------------------------------V----",
      "-----U---------------------------------------U-----",
      "------T-------------------------------------T------",
      "-------S-----------------------------------S-------",
      "--------R---------------------------------R--------",
      "---------Q-------------------------------Q---------",
      "----------P-----------------------------P----------",
      "-----------O---------------------------O-----------",
      "------------N-------------------------N------------",
      "-------------M-----------------------M-------------",
      "--------------L---------------------L--------------",
      "---------------K-------------------K---------------",
      "----------------J-----------------J----------------",
      "-----------------I---------------I-----------------",
      "------------------H-------------H------------------",
      "-------------------G-----------G-------------------",
      "--------------------F---------F--------------------",
      "---------------------E-------E---------------------",
      "----------------------D-----D----------------------",
      "-----------------------C---C-----------------------",
      "------------------------B-B------------------------",
      "-------------------------A-------------------------",
    ]);
  });
});
